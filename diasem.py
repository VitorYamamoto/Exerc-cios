num = int(input("Digite um número da semana: "))
if num == 0:
    print("Hoje é domingo. ")
elif num == 1:
    print("Hoje é segunda-feira. ")
elif num == 2:
    print("Hoje é terça-feita. ")
elif num == 3:
    print("Hoje é quarta-feira. ")
elif num == 4:
    print("Hoje é quinta-feira. ")
elif num == 5:
    print("Hoje é sexta-feira. ")
else:
    print("Hoje é sábado")